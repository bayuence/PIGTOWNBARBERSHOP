export async function createTransaction(data: {
  transaction_number?: string // Make optional, will be generated by database
  receipt_number?: string
  cashier_id?: number
  server_id?: number
  branch_id?: string
  cashier_name?: string
  server_name?: string
  branch_name?: string
  customer_name?: string
  subtotal: number
  discount_amount: number
  total_amount: number
  payment_method: string
  payment_status?: string
  notes?: string
}) {
  try {
    // Generate transaction number from database function if not provided
    let transactionNumber: string = data.transaction_number || ''
    
    if (!transactionNumber) {
      const { data: result, error: seqError } = await supabase
        .rpc('get_next_transaction_number')
      
      if (seqError || !result) {
        console.error('[createTransaction] Error getting transaction number:', seqError)
        // Fallback to timestamp-based number with Indonesian format if function fails
        const now = new Date()
        const dd = String(now.getDate()).padStart(2, '0')
        const mm = String(now.getMonth() + 1).padStart(2, '0')
        const yyyy = now.getFullYear()
        const timestamp = Date.now().toString().slice(-4)
        transactionNumber = `TRX-${dd}${mm}${yyyy}-${timestamp}`
      } else {
        transactionNumber = result as string
      }
    }
    
    // Generate receipt number if not provided (same format as transaction number)
    const receiptNumber = data.receipt_number || transactionNumber.replace('TRX-', 'RCP-')
    
    const { data: transaction, error } = await supabase
      .from('transactions')
      .insert({
        ...data,
        transaction_number: transactionNumber,
        receipt_number: receiptNumber,
        payment_status: data.payment_status || 'completed',
      })
      .select()
      .single()
    
    if (error) throw error
    return { data: transaction, error: null }
  } catch (error: any) {
    console.error('[createTransaction] Error:', error)
    return { data: null, error }
  }
}

