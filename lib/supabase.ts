export async function getKasbon(branchId?: string, statusFilter?: string) {
  try {
    // Get all kasbon
    const { data: kasbonData, error: kasbonError } = await supabase
      .from('kasbon')
      .select('*')
      .order('created_at', { ascending: false })
    
    if (kasbonError) throw kasbonError
    
    // Get all users
    const { data: users } = await supabase
      .from('users')
      .select('id, name, email, branch_id')
    
    // Create user map for quick lookup
    const userMap = new Map(users?.map(u => [u.id, u]) || [])
    
    // Combine kasbon with user data
    let kasbonWithUsers = (kasbonData || []).map(kasbon => ({
      ...kasbon,
      user: userMap.get(kasbon.user_id) || null
    }))
    
    // Filter by status if specified
    if (statusFilter && statusFilter !== 'all') {
      kasbonWithUsers = kasbonWithUsers.filter(k => k.status === statusFilter)
    }
    
    // Filter by branch if specified
    if (branchId && branchId !== 'all') {
      kasbonWithUsers = kasbonWithUsers.filter(k => k.user?.branch_id === branchId)
    }
    
    return { data: kasbonWithUsers, error: null }
  } catch (error: any) {
    console.error('[getKasbon] Error:', error)
    return { data: [], error }
  }
}

