export async function getPointTransactions(branchId?: string) {
  try {
    // Get all points
    const { data: points, error: pointsError } = await supabase
      .from('points')
      .select('*')
      .order('created_at', { ascending: false })
    
    if (pointsError) throw pointsError
    
    // Get all users
    const { data: users } = await supabase
      .from('users')
      .select('id, name, branch_id')
    
    // Get all branches
    const { data: branches } = await supabase
      .from('branches')
      .select('id, name')
    
    // Create maps for quick lookup
    const userMap = new Map(users?.map(u => [u.id, u]) || [])
    const branchMap = new Map(branches?.map(b => [b.id, b]) || [])
    
    // Combine data
    const transactions = (points || []).map(point => {
      const user = userMap.get(point.user_id)
      const branch = user?.branch_id ? branchMap.get(user.branch_id) : null
      
      return {
        ...point,
        users: user ? {
          id: user.id,
          name: user.name,
          branch_id: user.branch_id,
          branches: branch
        } : null
      }
    })
    
    // Filter by branch if specified and not "all"
    const filtered = (branchId && branchId !== 'all')
      ? transactions.filter(t => t.users?.branch_id === branchId)
      : transactions
    
    return { data: filtered, error: null }
  } catch (error: any) {
    console.error('[getPointTransactions] Error:', error)
    return { data: [], error }
  }
}

